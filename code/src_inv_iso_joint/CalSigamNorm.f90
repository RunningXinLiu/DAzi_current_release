! subroutine CalDdatSigma(dall,obst,cbst,sigmaT,meandeltaT,stddeltaT)
subroutine CalDdatSigma(dall, obst, cbst, sigmaT, meandeltaT)
implicit none
! abandoned
! used in Yunnan
! INPUT
integer dall
real obst(dall),cbst(dall)
! OUTPUT
real  sigmaT(dall)
real meandeltaT,stddeltaT
! PARAMETER
integer jj,kk,ii
integer i,j,k
real twostdratio
real :: deltaT(dall)
real, parameter :: obs_floor = 1.0e-6
real, parameter :: std_floor = 1.0e-6
real, parameter :: sigma_floor = 1.0e-6
! sigma(i)=std(t_res/t_obs)*t_obs
deltaT=0
meandeltaT=0

do i = 1,dall
    deltaT(i)=abs(cbst(i))/max(abs(obst(i)), obs_floor)
    meandeltaT=meandeltaT+deltaT(i)
enddo
meandeltaT=meandeltaT/dall
! meandeltaT=sum(deltaT(1:dall))/dall
stddeltaT=0
do i=1,dall
    stddeltaT=stddeltaT+(deltaT(i)-meandeltaT)**2
enddo
stddeltaT=max(sqrt(stddeltaT/dall), std_floor)
! stddeltaT=sqrt(sum((deltaT(1:dall)-meandeltaT)**2))
do i=1,dall
    twostdratio=abs(deltaT(i)/(1.5*stddeltaT))
    if (twostdratio.gt.1.0)then
        sigmaT(i)=stddeltaT*max(abs(obst(i)), obs_floor)*exp(twostdratio-1)
    else
        sigmaT(i)=stddeltaT*max(abs(obst(i)), obs_floor)
    endif
    sigmaT(i)=max(sigmaT(i), sigma_floor)
enddo
end subroutine

