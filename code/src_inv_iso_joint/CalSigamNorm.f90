! Active residual diagnostics use CalSparseReslNorm below.  The dense
! GVs/GGc/GGs routines are retained only for reading old experiments and for
! manual A/B checks against archived dense-matrix workflows.
!
! LEGACY-DENSE: do not call from active inversion code. These routines require
! dall*maxvp dense diagnostic matrices and defeat the sparse-memory path.
subroutine CalVsReslNorm(maxvp,dall,GVs,VsInv,datweight,Tdata,fwdTvs,resbst)
    use  lsmrblasInterface, only : dnrm2
    implicit none
    ! INPUT
    integer,intent(in):: dall,maxvp
    real,intent(in):: GVs(dall,maxvp)
    ! real,intent(in):: dv(maxvp),datweight(dall),Tdata(dall)
    real,intent(in):: VsInv(maxvp),datweight(dall),Tdata(dall)
    ! OUTPUT
    real:: resbst(dall)

    real:: resW2Nm,res2Nm
    real:: PreRes
    ! PARAMETER

    real fwdTgc(dall),fwdTgs(dall), fwdTvs(dall),fwdt(dall)
    real fwdTaa(dall),res(dall),resW(dall),per_res(dall)
    integer jj,kk,ii
    integer i,j,k
    !--------------------------------------------------------------------!
    ! Target:
    !  Calculate traveltime resiudal between observe traveltime
    !  and forward traveltime which is generated by inversion result
    !  using G from ref  model.
    !   r=Gm-d
    !--------------------------------------------------------------------!
    ! VsInv=dv(1:maxvp)
    !--------------------------------------------------------------------!
    ! forward residual
    fwdTvs=matmul(GVs,VsInv)
    fwdt=0
    res=0
    resW=0
    per_res=0
    resbst=0
    do i=1,dall
        resbst(i)=Tdata(i)-fwdTvs(i)
        resW(i)=resbst(i)*datweight(i) ! here, 1/sigma is for
        per_res(i)=resbst(i)/Tdata(i)
    enddo
    resW2Nm=dnrm2(dall,resW,1)
    res2Nm=dnrm2(dall,resbst,1)
    PreRes=sum(abs(per_res(1:dall)))/dall

    write(6 ,'(a,2f12.3)') '  dVs:  ||(Gm-d)||^2  and ||W(Gm-d)||^2: ', res2Nm, resW2Nm
    write(66,'(a,2f12.3)') '  dVs:  ||(Gm-d)||^2  and ||W(Gm-d)||^2: ', res2Nm, resW2Nm
    ! write(6,*) ' abs mean Res(Vs)/Res  (%)',PreRes*100
    ! write(66,*)' abs mean Res(Vs)/Res  (%)',PreRes*100
end subroutine

subroutine CalSparseReslNorm(m, n, maxvp, dall, leniw, lenrw, iw, rw, dv, datweight, Tdata, &
                             fwdTvs, fwdTaa, resbst, iso_inv)
    use  lsmrblasInterface, only : dnrm2
    implicit none
    integer,intent(in) :: m,n,maxvp,dall
    integer(kind=8),intent(in) :: leniw,lenrw
    integer,intent(in) :: iw(leniw)
    real,intent(in) :: rw(lenrw),dv(n),datweight(dall),Tdata(dall)
    logical,intent(in) :: iso_inv
    real,intent(out) :: fwdTvs(dall),fwdTaa(dall),resbst(dall)

    real,allocatable :: xpart(:),fwdAll(:),resW(:)
    real :: res2Nm,resW2Nm,mean
    integer :: i
    external aprod

    if ((.not. iso_inv) .and. (n .lt. 3*maxvp)) then
        stop 'CalSparseReslNorm: joint inversion requires n >= 3*maxvp'
    endif

    allocate(xpart(n),fwdAll(m),resW(dall))
    fwdTvs = 0.0
    fwdTaa = 0.0

    xpart = 0.0
    fwdAll = 0.0
    xpart(1:maxvp) = dv(1:maxvp)
    call aprod(1, m, n, xpart, fwdAll, leniw, lenrw, iw, rw)
    do i=1,dall
        if (abs(datweight(i)) <= tiny(datweight(i))) stop 'CalSparseReslNorm: zero data weight'
        fwdTvs(i) = fwdAll(i) / datweight(i)
    enddo

    if (.not. iso_inv) then
        xpart = 0.0
        fwdAll = 0.0
        xpart(maxvp+1:2*maxvp) = dv(maxvp+1:2*maxvp)
        call aprod(1, m, n, xpart, fwdAll, leniw, lenrw, iw, rw)
        do i=1,dall
            fwdTaa(i) = fwdAll(i) / datweight(i)
        enddo

        xpart = 0.0
        fwdAll = 0.0
        xpart(2*maxvp+1:3*maxvp) = dv(2*maxvp+1:3*maxvp)
        call aprod(1, m, n, xpart, fwdAll, leniw, lenrw, iw, rw)
        do i=1,dall
            fwdTaa(i) = fwdTaa(i) + fwdAll(i) / datweight(i)
        enddo
    endif

    resbst(1:dall) = Tdata(1:dall) - fwdTvs(1:dall) - fwdTaa(1:dall)
    resW(1:dall) = resbst(1:dall) * datweight(1:dall)
    resW2Nm = dnrm2(dall,resW,1)
    res2Nm = dnrm2(dall,resbst,1)

    if (iso_inv) then
        write(6 ,'(a,2f12.3)') '  dVs:  ||(Gm-d)||^2  and ||W(Gm-d)||^2: ', res2Nm, resW2Nm
        write(66,'(a,2f12.3)') '  dVs:  ||(Gm-d)||^2  and ||W(Gm-d)||^2: ', res2Nm, resW2Nm
    else
        write(6 ,'(a,2f12.3)') '  All:  ||(Gm-d)||^2  and ||W(Gm-d)||^2: ', res2Nm, resW2Nm
        write(66,'(a,2f12.3)') '  All:  ||(Gm-d)||^2  and ||W(Gm-d)||^2: ', res2Nm, resW2Nm
        write(66,'(a)')'  '
        write(6 ,'(a)')'  '
        mean=sum(abs(fwdTaa(1:dall)))/dall
        write(66,'(a,f12.4,a)')'  ABS Mean T(AA): ',mean,'s'
        write(6 ,'(a,f12.4,a)')'  ABS Mean T(AA): ',mean,'s'
        mean=sum(abs(fwdTvs(1:dall)))/dall
        write(66,'(a,f12.4,a)')'  ABS Mean T(dVs):',mean,'s'
        write(6 ,'(a,f12.4,a)')'  ABS Mean T(dVs):',mean,'s'
    endif

    deallocate(xpart,fwdAll,resW)
end subroutine


