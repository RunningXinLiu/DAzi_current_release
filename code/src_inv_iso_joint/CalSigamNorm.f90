subroutine CalGcsReslNorm(maxvp,dall,GGc,GGs, GcInv, GsInv, datweight,Tdata,fwdTaa,resbst)
    use  lsmrblasInterface, only : dnrm2
    implicit none
    ! resW2Nm: predicated ||W(Gm-d)||2
